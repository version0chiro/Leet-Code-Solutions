class Solution
{
    public:
    int solve(Node *head){
        if(!head) return 1;
        
        int carry = solve(head->next);
        
        int newC=head->data+carry;
        
        head->data=(newC)%10;
        
        return newC/10;
    }
    Node* addOne(Node *head) 
    {
        // Your Code here
        // return head of list after adding one
        
        int carry = solve(head);
        
        if(carry){
            Node* newHead = new Node(carry);
            newHead->next = head;
            head=newHead;
        }
        
        return head;
    }
};

